FULL_DECK = []


def _build_deck():
    if FULL_DECK:
        return FULL_DECK
    for card in MAJOR_ARCANA:
        FULL_DECK.append({**card, "type": "major"})
    for suit_name, suit_data in MINOR_ARCANA_TEMPLATES.items():
        for card_str in suit_data["cards"]:
            name = card_str.split(" — ")[0]
            meaning = card_str.split(" — ")[1]
            FULL_DECK.append({
                "name": name,
                "meaning": meaning,
                "type": "minor",
                "suit": suit_name,
            })
    return FULL_DECK


def draw_cards(n: int):
    deck = _build_deck()
    drawn = random.sample(deck, min(n, len(deck)))
    result = []
    for card in drawn:
        is_reversed = random.random() < 0.3
        result.append({
            "name": card["name"],
            "meaning": card["meaning"],
            "reversed": is_reversed,
        })
    return result
